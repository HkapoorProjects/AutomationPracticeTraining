package oOPsConceptPart1;

public class A {
	public A() {

		System.out.println("Parent Class");
	}

	public A(int i) {
		System.out.println("The value of i is: " + i);
	}

	public A(int i, int j) {
		System.out.println("The value of i is: " + i);
		System.out.println("The value of j is: " + j);
	}

}
