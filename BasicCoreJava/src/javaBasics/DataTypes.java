package javaBasics;

public class DataTypes {

	public static void main(String[] args) {

		// This is called single line comment
		/*
		 * Hi this is harshil laptop and soon he become automation tester
		 */
		// CTRL+SHIFT+? will help to mark multiple line comments :D
		// Data Types
		// 1. Integer----int---we can not write decimal value under integer data types

		int i = 10;
		int i1 = 20;
		int i2 = 100;
		System.out.println(i);
		System.out.println(i1);
		System.out.println(i + i1);
		// 2. Double---we can write integer value inside double but vice-versa is not
		// possible

		double d = 10;
		double d1 = 19.99;
		double d2 = 1;
		System.out.println(d2);
		// 3. Character--Char---we can write single character inside the single quotes,
		// it can be anything even a space is also allowed.

		char a = '1';
		char v = 'a';
		char c = ' ';
		System.out.println(c);
		// 4. String---String S start with capital letter while declaring and it is not
		// a data type it is a pre defined class

		String s = "Hi this is Harshil kapoor,a future automation tester";
		String s1 = "Hello";
		String s3 = " ";
		String s4 = "a";
		System.out.println(s);
		// 5. Boolean-- it is only used to check the condition it consist true and false
		// values.

		boolean b1 = true;
		boolean b2 = false;
		System.out.println(b1);
		// Primitive Data type---int,double,float,char,long,short,boolean,byte

	}

}
